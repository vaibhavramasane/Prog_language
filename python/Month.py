"""Q30. Write a java program to enter month number between(1-12) and print number of
 days in month using if else. How to print the number of days in a given month using
 if else in java programming.
"""

n = int(input("Enter Month:"));

if n==1 :
    print("Jan");
elif n==2:
    print("Feb");
elif n==3:
    print("March");
elif n==4:
    print("April");
elif n==5:
    print("May");
elif n==6:
    print("June");
elif n==7:
    print("July");
elif n==8:
    print("Aug");
elif n==9:
    print("Sept");
elif n==10:
    print("Oct");
elif n==11:
    print("Nov");
elif n==12:
    print("Dec");
else:
    print("Invalid Months: ");
    
  